def count_squares(m, n):
    """
    Рекурсивная процедура для подсчета количества вырезанных квадратов.

    :param m: сторона прямоугольной части листа по вертикали
    :param n: сторона прямоугольной части листа по горизонтали
    :return: количество вырезанных квадратов
    """
    # Базовый случай: если одна из сторон прямоугольника равна 0, то больше нельзя вырезать квадраты.
    if m == 0 or n == 0:
        return 0

    # Находим минимальную сторону
    min_side = min(m, n)

    # Вырезаем квадрат и вызываем процедуру для оставшейся части
    return 1 + count_squares(min_side, max(m, n) - min_side)

# Пример использования
M = int(input("Введите высоту листа бумаги (M): "))
N = int(input("Введите ширину листа бумаги (N): "))

# Вызываем процедуру и выводим результат
result = count_squares(M, N)
print(f"Количество вырезанных квадратов: {result}")
